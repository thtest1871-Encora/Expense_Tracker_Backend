package com.categoryservice.main.dto;

public class CategoryResponse {

    private Long id;
    private String name;
    private String type;
    private String emoji;

    public CategoryResponse(Long id, String name, String type, String emoji) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.emoji = emoji;
    }

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getEmoji() {
		return emoji;
	}

	public void setEmoji(String emoji) {
		this.emoji = emoji;
	}

    // getters + setters
}